package com.mindsprint.project1.basics;

public class Assignment {
    public static void main(String[] args) {
        int num1=10;
        System.out.println(" value:" +num1);
        num1+=2; // num1=num1+2
        System.out.println(" after increment value:" +num1);
        num1-=2; // num1=num1-2
        System.out.println(" after decrement value:" +num1);


    }
}
