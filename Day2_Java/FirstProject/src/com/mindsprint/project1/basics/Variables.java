package com.mindsprint.project1.basics;

public class Variables {
    public static void main(String[] args) {
        int num1=20;
        float num2=34.78f;// indicate f for float number
        double num3=89.90; // for double no need to mention f
        char c='A';
        boolean check=78>90;
        System.out.println("Number1:" +num1);
        System.out.println("Number2:" +num2);
        System.out.println("Number3:"+num3);
        System.out.println("CHARACTER:"+c);
        System.out.println("boolean:"+check);
    }
}
